#OPERATORS

#Arithmetic Operators
print("2+3=", 2+3)
print("4*5=", 4*5)
print("2**4=", 2**4)
print("5/2=", 5/2) #Division
print("5//2=", 5//2) #Integer Division
print("5%2=", 5%2) #Modulus

#Relational Operators
print("2>5=", 2>5)
print("2<5=", 2<5)
print("5!=6=", 5!=6)
print("5==5=", 5==5)
print("4>=4.567 =", 4>=4.567)

